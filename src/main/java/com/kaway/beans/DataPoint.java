package com.kaway.beans;

public class DataPoint {
    private String time;
    private float value;

    public DataPoint(String time, float value) {
        this.time = time;
        this.value = value;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }
}
